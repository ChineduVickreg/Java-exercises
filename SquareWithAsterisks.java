package Cohort3.com.Exercises;

public class SquareWithAsterisks {
   public static void main(String[]args){
       int length = 4;
       for (int i = 1; i <= length; i++){

           for(int t = 1; t <= length; t++){
               System.out.print("*");
           }
           System.out.println(" ");
       }

   }
}
